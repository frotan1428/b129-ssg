package ssg20methodcreationandmethodoverloading;

public class Calcualator {

    // Let's create a method that subtracts the value of two variables
    public static void addition(int a,int b){
        System.out.println(a+b);
    }

    public static void Subtraction(int a,int b){
        System.out.println(a-b);
    }

    // Let's create a method that divides the value of two variables
    public static void divided(int a,int b){
        System.out.println(a/b);
    }
    // Let's create a method that multiplies the values of two variables
    public static void product(int a,int b){
        System.out.println(a*b);
    }
}
